fruit = 'banana'
print('Length of fruit string',len(fruit))
print(fruit[3],'\n')


# slice the string fruit (while loop)
print('slice the string fruit (while loop)')
index=0
while index < len(fruit):
    print(fruit[index])
    index+=1

#slice the string fruit (for loop)
print('slice the string fruit (for loop)')
for letter in fruit:
        print(letter)



